    private void update_film(){
        
        String query = "SELECT titolo from orari Where giorno ='"+giorno+"'";
        
        try {        
            st = cn.prepareStatement(query);
            rs = st.executeQuery();
            
            while (rs.next()){
                String titolo =rs.getString("titolo");
                film.addItem(titolo);
            }
        } catch (SQLException ex) {
            System.out.println(ex);
        }
            
            
        
    }
