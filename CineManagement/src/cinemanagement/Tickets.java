    private void filmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_filmActionPerformed
        String fil = (String) film.getSelectedItem();
        String query = "SELECT * from orari Where giorno ='"+giorno+"' AND titolo='"+fil+"'";
        String sal = "";
        String hour = "";
        try {
            st = cn.prepareStatement(query);
            rs = st.executeQuery();
            rs.next();
            sal = rs.getString("sala");
            hour = rs.getString("Ora");
            
        } catch (SQLException ex) {
            System.out.println(ex);
        }
            int count=0;
            while (count<2){
                String par = (String)sala.getItemAt(count);
                if (sal.equalsIgnoreCase(par)){
                    sala.setSelectedIndex(count);
                    break;
                }
                count++;
            }
            count =0;
            while (count<2){
                String par = (String)oreBox.getItemAt(count);
                if (hour.equalsIgnoreCase(par)){
                    oreBox.setSelectedIndex(count);
                    count=0;
                    break;
                }
                count++;
            }
            
            
            if ((sal.equalsIgnoreCase("1"))&&(hour.equalsIgnoreCase("16:30"))){
                seatSala="sala1";
            }
            if ((sal.equalsIgnoreCase("1"))&&(hour.equalsIgnoreCase("19:30"))){
                seatSala="sala2";
            }
            if ((sal.equalsIgnoreCase("2"))&&(hour.equalsIgnoreCase("16:30"))){
                seatSala="sala3";
            }
            if ((sal.equalsIgnoreCase("2"))&&(hour.equalsIgnoreCase("19:30"))){
                seatSala="sala4";
            }
            updatePosti(seatSala);
    }//GEN-LAST:event_filmActionPerformed

